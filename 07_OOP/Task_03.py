'''
Реализовать программу работы с органическими клетками.

Необходимо создать класс Клетка.
В его конструкторе инициализировать параметр, соответствующий количеству клеток (целое число).
В классе должны быть реализованы методы перегрузки арифметических операторов:
сложение (__add__()), вычитание (__sub__()), умножение (__mul__()), деление (__truediv__()).

Данные методы должны применяться только к клеткам и выполнять увеличение, уменьшение,
умножение и обычное (не целочисленное) деление клеток, соответственно. В методе деления должно осуществляться округление
значения до целого числа.

Сложение. Объединение двух клеток. При этом число ячеек общей клетки должно равняться сумме ячеек исходных двух клеток.

Вычитание. Участвуют две клетки. Операцию необходимо выполнять только если разность количества ячеек двух клеток
больше нуля, иначе выводить соответствующее сообщение.

Умножение. Создается общая клетка из двух. Число ячеек общей клетки определяется как произведение количества ячеек
этих двух клеток.

Деление. Создается общая клетка из двух. Число ячеек общей клетки определяется как целочисленное деление количества
ячеек этих двух клеток.

В классе необходимо реализовать метод make_order(), принимающий экземпляр класса и количество ячеек в ряду.
Данный метод позволяет организовать ячейки по рядам.
Метод должен возвращать строку вида *****\n*****\n*****..., где количество ячеек между \n равно переданному аргументу.
Если ячеек на формирование ряда не хватает, то в последний ряд записываются все оставшиеся.

Например, количество ячеек клетки равняется 12, количество ячеек в ряду — 5. Тогда метод make_order() вернет строку:
*****\n*****\n**.

Или, количество ячеек клетки равняется 15, количество ячеек в ряду — 5. Тогда метод make_order() вернет строку:
*****\n*****\n*****.
'''


class Cell:

    def __init__(self, nums: int):
        self.nums = nums # колличество клеток

    def __str__(self):
        return f'{self.nums}'

    def __add__(self, other):
        return Cell(self.nums + other.nums)

    def __sub__(self, other):
        if self.nums > other.nums:
            return Cell(self.nums - other.nums)
        else:
            return 'Результат - ноль или отрицательное число'

    def __mul__(self, other):
        return Cell(self.nums * other.nums)

    def __truediv__(self, other):
        return Cell(self.nums // other.nums)

    # вариант №1 метода make_order
    # def make_order(self, n):
    #     lines = self.nums // n
    #     piece = self.nums - self.nums // n * n
    #     for i in range(lines):
    #         print('*' * n)
    #     print('*' * piece)

    # вариант №2 метода make_order
    def make_order(self, n):
        result = ''
        for i in range(self.nums):
            if i % n != 0 or i == 0:
                result += '*'
            else:
                result += '\n*'
        return result


cell_01 = Cell(5)
cell_02 = Cell(9)

print(cell_01 + cell_02) # сложение
print(cell_01 - cell_02) # вычитание
print(cell_01 * cell_02) # умножение
print(cell_01 / cell_02) # деление

print(cell_02.make_order(5)) # визуальное представление количства клеток
